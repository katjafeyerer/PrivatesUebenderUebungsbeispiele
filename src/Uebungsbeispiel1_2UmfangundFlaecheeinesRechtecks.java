public class Uebungsbeispiel1_2UmfangundFlaecheeinesRechtecks {
    public static void main(String[] args) {
        System.out.println("Hello World");
        int breite = 2;
        int laenge = 3;

        int umfang = 2*laenge + 2*breite;
        System.out.println("Der Umfang des Rechteckts beträgt: " + umfang);

        int flaeche = laenge * breite;
        System.out.println("Die Fläche des Rechteckts beträgt: " + flaeche);

    }
}
